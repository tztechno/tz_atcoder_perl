#!/usr/bin/perl

use strict;
use warnings;

my $n = <STDIN>;
chomp $n;
my $ans = 0;

for my $i (0..$n) {
    if (2**$i <= $n) {
        $ans = 2**$i;
    } else {
        last;
    }
}
print "$ans\n";  
