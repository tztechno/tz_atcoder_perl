$max_count = 0;
for my $num (1..$input) {
    my $n = $num;
    my $count = 0;
    while ($n % 2 == 0) {
        $count++;
        $n /= 2;
    }
    if ($count > $max_count) {
        $max_count = $count;
        $ans = $num;
    }
}

